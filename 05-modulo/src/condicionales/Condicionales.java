package condicionales;

public class Condicionales {

    public static void main(String[] args) {
        int a = 1, b = 2;
        int c = a * b;
        if (c >= 0) {
            System.out.println("El resultado es un número positivo");
        } else {
            System.out.println("El resultado es un número negativo");
        }
    }
}
